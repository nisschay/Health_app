const warnedTests = new Set<string>();

function titleCaseFallback(raw: string): string {
  return raw
    .trim()
    .split(/\s+/)
    .map((chunk) => chunk.charAt(0).toUpperCase() + chunk.slice(1).toLowerCase())
    .join(" ");
}

function normalizeTestLookupKey(raw: string): string {
  return raw
    .toLowerCase()
    .trim()
    .replace(/[()]/g, "")
    .replace(/[_]+/g, " ")
    .replace(/\s*\/\s*/g, " / ")
    .replace(/[^a-z0-9\-&/+\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function normalizeTestName(raw: string | null | undefined): string {
  if (!raw || !String(raw).trim()) return "Unknown Test";

  const original = String(raw).trim();
  const key = normalizeTestLookupKey(original);
  const exact = TEST_NAME_MAP[key];
  if (exact) return exact;

  const compactKey = key.replace(/\s+/g, "");
  for (const [mapKey, canonical] of Object.entries(TEST_NAME_MAP)) {
    const compactMapKey = mapKey.replace(/\s+/g, "");
    if (compactKey === compactMapKey) {
      return canonical;
    }
  }

  for (const [mapKey, canonical] of Object.entries(TEST_NAME_MAP)) {
    const compactMapKey = mapKey.replace(/\s+/g, "");
    if (
      key.includes(mapKey)
      || mapKey.includes(key)
      || compactKey.includes(compactMapKey)
      || compactMapKey.includes(compactKey)
    ) {
      return canonical;
    }
  }

  if (!warnedTests.has(original)) {
    warnedTests.add(original);
    console.warn(`[TestName] Unmapped test: "${original}" - consider adding to TEST_NAME_MAP`);
  }

  return titleCaseFallback(original);
}
